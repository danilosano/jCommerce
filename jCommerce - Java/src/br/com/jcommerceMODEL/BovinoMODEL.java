package br.com.jcommerceMODEL;

/**
 *
 * @author Aluno(a)
 */
public class BovinoMODEL {
    private int codBovino;
    private String nomeBovino;
    private String raça;
    private int peso;

    /**
     * @return the codBovino
     */
    public int getCodBovino() {
        return codBovino;
    }

    /**
     * @param codBovino the codBovino to set
     */
    public void setCodBovino(int codBovino) {
        this.codBovino = codBovino;
    }

    /**
     * @return the nomeBovino
     */
    public String getNomeBovino() {
        return nomeBovino;
    }

    /**
     * @param nomeBovino the nomeBovino to set
     */
    public void setNomeBovino(String nomeBovino) {
        this.nomeBovino = nomeBovino;
    }

    /**
     * @return the raça
     */
    public String getRaça() {
        return raça;
    }

    /**
     * @param raça the raça to set
     */
    public void setRaça(String raça) {
        this.raça = raça;
    }

    /**
     * @return the peso
     */
    public int getPeso() {
        return peso;
    }

    /**
     * @param peso the peso to set
     */
    public void setPeso(int peso) {
        this.peso = peso;
    }
}

    private void preencheTabela()
    {
        try
        {
            EstadoCTR estadoCTR = new EstadoCTR();
            ResultSet rsEstadoTabela = estadoCTR.listaEstado();
            
            String[] todos = {"CÃ³digo", "Nome Estado", "Sigla Estado"};
            String[][] linhas = {};
            
            DefaultTableModel tableModel = new DefaultTableModel(linhas, todos){
                
                public boolean isCellEditTable(int rowIndex, int mColIndex){ 
                    return false;
                }
            };
            
            Vector<Vector> dados = new Vector<>();
            
            while(rsEstadoTabela.next()){
                
                Vector regVetor = new Vector();
                regVetor.add(rsEstadoTabela.getInt("codestado"));
                regVetor.add(rsEstadoTabela.getString("nomeestado"));
                regVetor.add(rsEstadoTabela.getString("siglaestado"));
                
                dados.add(regVetor);
                tableModel.addRow(regVetor);
            }
            
            tblEstado.setModel(tableModel);
            
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }
    
    public void pegaValorSelecionado() 
    {
        codEstado = String.valueOf(tblEstado.getValueAt(tblEstado.getSelectedRow(), 0));
        this.dispose();
    }
    
    public void buscaEstado() 
    {
        EstadoCTR estadoCTR = new EstadoCTR();
        rsRegistros = estadoCTR.pesquisarEstado(txtPesquisaEstado.getText());
        preencheTabela();
    }
    
    public void acaoBtnBuscar()
    {
        buscaEstado();
        if (tblEstado.getRowCount() > 0) 
        {
            tblEstado.changeSelection(0, 0, false, false);
        }
        tblEstado.grabFocus();
    }
    
    

